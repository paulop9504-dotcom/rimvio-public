export type OverseasPlaceKind = "country" | "city";

export type OverseasPlaceEntry = {
  pattern: RegExp;
  label: string;
  countryLabel: string;
  geocodeQuery: string;
  lat: number;
  lng: number;
  kind: OverseasPlaceKind;
};

function escapeRegex(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function termToRegex(term: string): string {
  return escapeRegex(term).replace(/\s+/g, "\\s*");
}

/** Word-boundary match for Korean / Latin place tokens. */
export function buildPlacePattern(...terms: string[]): RegExp {
  const alts = terms.map(termToRegex).join("|");
  return new RegExp(`(?:^|[\\s,·])(?:${alts})(?:[\\s,·]|$)`, "iu");
}

function city(
  label: string,
  countryLabel: string,
  geocodeQuery: string,
  lat: number,
  lng: number,
  ...terms: string[]
): OverseasPlaceEntry {
  return {
    pattern: buildPlacePattern(...(terms.length > 0 ? terms : [label])),
    label,
    countryLabel,
    geocodeQuery,
    lat,
    lng,
    kind: "city",
  };
}

function country(
  label: string,
  geocodeQuery: string,
  lat: number,
  lng: number,
  ...terms: string[]
): OverseasPlaceEntry {
  return {
    pattern: buildPlacePattern(...(terms.length > 0 ? terms : [label])),
    label,
    countryLabel: label,
    geocodeQuery,
    lat,
    lng,
    kind: "country",
  };
}

